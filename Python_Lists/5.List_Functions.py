# Functions of LIST in Python

listFunction = [99, 15, 1, 0.96, 17.49]

print(f"sort(): {listFunction.sort()}")
print(f"reverse(): {listFunction.sort()}")
print(f"append(): {listFunction.append(9)}")
print(f"appended new value: {listFunction}")
print(f"insert(): {listFunction.insert(3, 3.14)}")
print(f"inserted new value: {listFunction}")
print(f"pop(): {listFunction.pop(2)}")
print(f"Element popped: {listFunction}")
print(f"remove(): {listFunction.remove(1)}")
print(f"Element removed: {listFunction}")