# The "List" will be sorted internally, and output of sorted will always be "NONE".

# Sorting
sortList = [41, 59, 17, 3, 67, 51, 75, 98]
print(f"Original list: {sortList}")

# Output in NONE
print(f"Sorted list:{sortList.sort()}")

# Reverse
# Output in NONE
print(f"Reversed list: {sortList.sort(reverse= True)}")
