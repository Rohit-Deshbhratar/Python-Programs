# Basic List operations

# Empty list
emptyList = []

# List with characters
charList = ['a', 'b', 'c']

# List with numbers
numList = [1, 2, 6]

# List with alphanumeric values
alphaList = ['a', 'b', 17, 48]

# Length of lists
print(f"Length of empty list: {emptyList}")
print(f"Length of character list: {charList}")
print(f"Length of numbers list: {numList}")
print(f"Length of alphanumeric list: {alphaList}")

# Datatype of List
print(f"Data type: {type(numList)}")

# Length of list
print(f"Length of empty list: {len(emptyList)}")
print(f"Length of another list: {len(charList)}")