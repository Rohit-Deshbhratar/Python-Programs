# Nested dictionary

country = {'india' : {'states' : 'maharashtra', 'unionTerritory' : 'Pondicherry'},
           'world' : {'continent' : 'Africa, America, Antarctica, Asia, Australia, Europe,', 
                      'oceans' : 'Pacific, Arctic, Indian Ocean'}}
print(country)

# OR/Another way is, assign dictionary to another dictionary

myDict = {'country' : country}
print('\n', myDict)