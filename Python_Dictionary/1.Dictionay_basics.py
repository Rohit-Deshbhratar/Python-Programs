# Basic structure of DICTIONARY using dict() constructor

thisDict = dict(name = 'rohit', birthPlace = 'chandrapur', job = 'software tester')
print(thisDict)

# Basic format of DICTIONARY

carInfo = {'brand' : 'Ford', 'model' : 'Mustang', 'Year' : 1964}
print(carInfo)

# Changing values in DICTIONARY

carInfo['Year'] = 2023
print(carInfo)

# Adding an item into DICTIONARY

carInfo['color'] = 'red'
print(carInfo)